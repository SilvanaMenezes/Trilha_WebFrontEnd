// Operadores Aritméticos

let n1 = 20
let n2 = 2

//Adição
const soma = n1 + n2
console.log('Soma = ', soma)

//Subtração
console.log('Subtração =', n1 - n2)

//Multiplicação
console.log('Multiplicação =', n1 * n2)

//Divisão
console.log('Divisão =', n1 / n2)

//Exponenciação
console.log('Exponenciação =', n1 ** n2)

//Resto da Divisão
console.log('Resto da Divisão =', n1 % 3)

// Precedência de Operadores
console.log( 3 + 7 * 3)
console.log((3 + 7) * 3)

// Biblioteca Math
console.log('Valor de PI = ', Math.PI)
console.log('Raiz Quadrada de 16 = ', Math.sqrt(16))